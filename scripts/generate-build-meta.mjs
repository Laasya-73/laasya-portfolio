import { execFileSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const outputFile = resolve('src/lib/generated/build-meta.ts');

const readExistingValue = (name) => {
  try {
    const current = readFileSync(outputFile, 'utf8');
    const regex = new RegExp(`export const ${name} = "([^"]+)";`);
    return current.match(regex)?.[1] ?? '';
  } catch {
    return '';
  }
};

const runGit = (args) => {
  try {
    return execFileSync('git', args, { stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim();
  } catch {
    return '';
  }
};

const existingDateISO = readExistingValue('LAST_UPDATED_ISO');
const existingSha = readExistingValue('COMMIT_SHA');

const commitDateISO = runGit(['log', '-1', '--format=%cI']) || existingDateISO || new Date().toISOString();
const commitSha = runGit(['rev-parse', '--short', 'HEAD']) || existingSha || 'local';

mkdirSync(dirname(outputFile), { recursive: true });

const fileContents = `// Auto-generated by scripts/generate-build-meta.mjs
export const LAST_UPDATED_ISO = ${JSON.stringify(commitDateISO)};
export const COMMIT_SHA = ${JSON.stringify(commitSha)};
`;

writeFileSync(outputFile, fileContents);
