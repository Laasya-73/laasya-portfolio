import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const outputFile = resolve('src/lib/generated/build-meta.ts');

const runGit = (args) => {
  try {
    return execFileSync('git', args, { stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim();
  } catch {
    return '';
  }
};

const normalizeIsoDate = (value) => {
  if (!value) return '';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '';
  return date.toISOString();
};

const envSha =
  process.env.VERCEL_GIT_COMMIT_SHA?.slice(0, 7) ||
  process.env.GITHUB_SHA?.slice(0, 7) ||
  '';
const envDate = normalizeIsoDate(
  process.env.VERCEL_GIT_COMMIT_DATE ||
  process.env.VERCEL_GIT_COMMIT_TIMESTAMP ||
  process.env.GITHUB_EVENT_HEAD_COMMIT_TIMESTAMP ||
  ''
);

const gitSha = runGit(['rev-parse', '--short', 'HEAD']);
const gitDate = normalizeIsoDate(runGit(['log', '-1', '--format=%cI']));

const commitSha = gitSha || envSha || 'local';
const commitDateISO = gitDate || envDate || new Date().toISOString();

mkdirSync(dirname(outputFile), { recursive: true });

const fileContents = `// Auto-generated by scripts/generate-build-meta.mjs
export const LAST_UPDATED_ISO = ${JSON.stringify(commitDateISO)};
export const COMMIT_SHA = ${JSON.stringify(commitSha)};
`;

writeFileSync(outputFile, fileContents);
